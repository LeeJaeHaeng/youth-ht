def main() -> int:
    if not KEY:
        print("[WARN] KAKAO_REST_KEY 미설정 — static 좌표만 사용")

    # 268 시군구 로드
    targets: list[tuple[str, str, str]] = []
    with SIGUNGU_CSV.open("r", encoding="utf-8-sig") as f:
        for r in csv.DictReader(f):
            targets.append((r["code"], r["name"], r["sido"]))
    print(f"  {len(targets)} 시군구 처리")

    # 기존 parquet 로드 (resume support)
    existing: dict[str, dict] = {}
    if OUT.exists():
        ex_df = pl.read_parquet(OUT)
        for row in ex_df.to_dicts():
            existing[row["sigungu_code"]] = row
        print(f"  기존 좌표 {len(existing)}건 로드 (resume)")

    rows: list[dict] = list(existing.values())
    fails = []

    with httpx.Client() as client:
        for i, (code, name, sido) in enumerate(targets, 1):
            if code in existing:
                continue  # 이미 조회됨 → skip

            # 1. static lookup
            if code in STATIC_COORDS:
                lat, lng = STATIC_COORDS[code]
                rows.append({"sigungu_code": code, "name": name, "sido": sido,
                             "lat": lat, "lng": lng})
                continue

            # 2. Kakao API
            if not KEY:
                fails.append(f"{code} {name}")
                continue

            ll = resolve(client, name)
            if ll is None:
                fails.append(f"{code} {name}")
                print(f"  [MISS] {i:3d} {code} {name}")
                continue
            rows.append({"sigungu_code": code, "name": name, "sido": sido,
                         "lat": ll[0], "lng": ll[1]})
            if i % 25 == 0 or i <= 5:
                print(f"  [{i:3d}/{len(targets)}] {code} {name} → ({ll[0]:.5f}, {ll[1]:.5f})")
            time.sleep(0.05)

    if not rows:
        print("[WARN] 결과 0건 — parquet 유지")
        return 1

    df = pl.DataFrame(rows)
    df.write_parquet(OUT, compression="zstd")
    print(f"\n[DONE] {len(df)} 시군구 → {OUT}")
    if fails:
        print(f"[WARN] 좌표 부여 실패 {len(fails)}건")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
