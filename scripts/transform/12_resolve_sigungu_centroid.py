def search_address(client: httpx.Client, query: str) -> tuple[float, float] | None:
    """주소 검색 — 행정구역명 정확도 가장 높음."""
    headers = {"Authorization": f"KakaoAK {KEY}"}
    r = client.get(API_ADDR, headers=headers, params={"query": query, "size": 1}, timeout=15)
    if r.status_code != 200:
        return None
    docs = r.json().get("documents") or []
    if not docs:
        return None
    d = docs[0]
    return float(d["y"]), float(d["x"])


def search_keyword(client: httpx.Client, query: str) -> tuple[float, float] | None:
    """키워드 검색 fallback — '강남구청' 같은 청사 검색."""
    headers = {"Authorization": f"KakaoAK {KEY}"}
    r = client.get(API_KW, headers=headers, params={"query": query, "size": 1}, timeout=15)
    if r.status_code != 200:
        return None
    docs = r.json().get("documents") or []
    if not docs:
        return None
    return float(docs[0]["y"]), float(docs[0]["x"])


SIDO_ALIASES = {
    "강원특별자치도": "강원도",
    "전북특별자치도": "전라북도",
    "전라북도": "전북특별자치도",
    "제주특별자치도": "제주도",
}


def resolve(client: httpx.Client, name: str) -> tuple[float, float] | None:
    """다단계 fallback: 주소(원본/별칭) → 청사 키워드 → 마지막 단어."""
    # 1. 원본 주소
    if (ll := search_address(client, name)):
        return ll
    # 2. 시도 별칭 변환 (강원특별자치도→강원도 등)
    for old, new in SIDO_ALIASES.items():
        if name.startswith(old):
            alias = name.replace(old, new, 1)
            if (ll := search_address(client, alias)):
                return ll
            if (ll := search_keyword(client, alias)):
                return ll
    # 3. 청사 키워드 (마지막 단어 시군구청 패턴)
    tokens = name.split()
    last_token = tokens[-1]
    if last_token.endswith("구") and len(tokens) >= 2:
        # 행정구는 "마산회원구" 같이 시명 + 구명 조합
        chong = f"{tokens[-2]} {last_token}청"
    else:
        chong = last_token + ("청" if last_token.endswith(("구", "시", "군")) else "")
    if (ll := search_keyword(client, chong)):
        return ll
    # 4. 마지막 단어 단독
    return search_keyword(client, last_token)


