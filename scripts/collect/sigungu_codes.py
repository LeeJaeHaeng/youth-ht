def load_all_sigungu(csv_path: str | None = None) -> list[tuple[str, str]]:
    """CSV에서 전국 시군구 코드 로드.

    파일이 없으면 KNOWN_METRO (수도권+광역시, 약 100개)로 fallback.
    완전한 250개를 사용하려면 사용자가 data/raw/sigungu_codes.csv를 다운로드.
    """
    from pathlib import Path

    import polars as pl

    p = Path(csv_path) if csv_path else None
    if p is None or not p.exists():
        print(f"[INFO] {p} 없음 — KNOWN_METRO {len(KNOWN_METRO)}개로 fallback")
        print("       전국 csv 다운로드 가이드: docs/user_action_required.md §D")
        return KNOWN_METRO

    df = pl.read_csv(p)
    return list(zip(df["code"].cast(pl.Utf8).to_list(), df["name"].to_list()))


